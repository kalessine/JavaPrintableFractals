    private void jbDelFormulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDelFormulaActionPerformed
        Object o = jlFractalFormulas.getSelectedValue();
        if( o == null ) return;
        if( jlFractalFormulas.getSelectedIndex() == 0 ) {
            JOptionPane.showMessageDialog(this,"Please Use 'set first formula to change first formula","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if( o instanceof MergeFormula ) {
            int indx = jlFractalFormulas.getSelectedIndex();
            fractal.getFormulaArray().remove(indx);// Merge Formula
            fractal.getFormulaArray().remove(indx);// Formula
        }
        if( o instanceof Formula ) {
            int indx = jlFractalFormulas.getSelectedIndex();
            fractal.getFormulaArray().remove(indx-1);// Merge Formula
            fractal.getFormulaArray().remove(indx-1);// Formula
        }
        jlFractalFormulas.clearSelection();
        fractalFormulaChanged(null);
    }//GEN-LAST:event_jbDelFormulaActionPerformed
    
    private void jbSetFirstFormulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSetFirstFormulaActionPerformed
        Formula formula = (Formula)jlAllFormulas.getSelectedValue();
        if( formula == null ) return;
        if( fractalFormulasModel.getSize() == 0 ){
            fractal.getFormulaArray().add(formula.clone());
        }
        if( fractalFormulasModel.getSize() > 0 ) {
            fractal.getFormulaArray().set(0,formula.clone());
        }
        fractalFormulasModel.fireUpdate();
        fractalFormulaChanged(null);
    }//GEN-LAST:event_jbSetFirstFormulaActionPerformed
    
    private void jbSaveBeanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveBeanActionPerformed
        int indx = jlFractalFormulas.getSelectedIndex();
        if( indx == -1 ) {
            this.propertyEditor.setBean(null);
            return;
        }
        Object o = jlFractalFormulas.getSelectedValue();
        this.propertyEditor.load(o);
    }//GEN-LAST:event_jbSaveBeanActionPerformed
    
