    private void fractalFormulaChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_fractalFormulaChanged
        int indx = jlFractalFormulas.getSelectedIndex();
        if( indx == -1 ) {
            this.propertyEditor.setBean(null);
            return;
        }
        Object o = jlFractalFormulas.getSelectedValue();
        this.propertyEditor.setBean(o);
        propertyEditor.invalidate();
        propertyEditor.validate();
        propertyEditor.repaint();
    }//GEN-LAST:event_fractalFormulaChanged
    
    private void jbAddFormulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddFormulaActionPerformed
        Formula formula = (Formula)jlAllFormulas.getSelectedValue();
        if( formula == null ) return;
        if( fractalFormulasModel.getSize() == 0 ){
            fractal.getFormulaArray().add(formula.clone());
        }
        if( fractalFormulasModel.getSize() > 0 ) {
            fractal.getFormulaArray().add(new MergeFormula());
            fractal.getFormulaArray().add(formula.clone());
        }
        fractalFormulasModel.fireUpdate();
        fractalFormulaChanged(null);
    }//GEN-LAST:event_jbAddFormulaActionPerformed
    
    
